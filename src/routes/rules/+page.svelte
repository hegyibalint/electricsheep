<div class="mx-auto text-center flex flex-col gap-5 h-fit">
	<h1 class="text-3xl font-bold underline m-5">The Rules</h1>
	
	<h3 class="text-xl font-bold">Main Goal</h3>
	<p>
		Try to tell if your opponent is a robot or a human player,
		while trying to convince them that you're a robot.
	</p>
	<h3 class="text-xl font-bold mt-3">Game mechanic</h3>
	<p>
		Chats are made of 10 turns per player. Messages can be sent in turns only.
		Once you are confident of the identity of your opponent, press the
		respective button below the chat to set your guess.
	</p>
	<p>
		Once both players have guessed or the 10 turns are over, you will
		discover the identity of your opponent and points are assigned.
	</p>
	<p>
		No guess is automatic failure.
	</p>
	<h3 class="text-xl font-bold mt-3">Point system</h3>
	<p>
		You get one point by guessing if your opponent is an AI or a real player,
		and another point if your opponent guesses your identity wrong.
	</p>
	<p>
		So try to convince them you're a robot :)		
	</p>
	
</div>
